    private void jComboBox4ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox4ItemStateChanged
        if (this.jCheckBox4.isSelected()) {
            this.jCheckBox5.setSelected(false);
            this.jCheckBox6.setSelected(false);
            this.jCheckBox7.setSelected(false);
            this.jCheckBox8.setSelected(false);
            if ("101".equals((String) this.jComboBox4.getSelectedItem())) {
                this.jCheckBox5.setSelected(true);
                this.jTextField1.setText("PlayCard");
                this.jTextField2.setText("5");
                this.jCheckBox6.setSelected(true);
                this.jTextField3.setText("Direction");
                this.jTextField4.setText("F");
            } else if ("102".equals((String) this.jComboBox4.getSelectedItem())) {
                this.jCheckBox5.setSelected(true);
                this.jTextField1.setText("PlayCard");
                this.jTextField2.setText("4");
                this.jCheckBox6.setSelected(true);
                this.jTextField3.setText("NumOfCard");
                this.jTextField4.setText("2");
            } else if ("103".equals((String) this.jComboBox4.getSelectedItem())) {
            }

        }
    }//GEN-LAST:event_jComboBox4ItemStateChanged

