    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            HashMap<String, String> response = new HashMap<>();
            if (this.jCheckBox2.isSelected()) {
                response.put("From", (String) this.jComboBox2.getSelectedItem());
            }
            if (this.jCheckBox3.isSelected()) {
                response.put("To", (String) this.jComboBox3.getSelectedItem());
            }
            if (this.jCheckBox1.isSelected()) {
                response.put("Type", (String) this.jComboBox1.getSelectedItem());
            }
            if (this.jCheckBox4.isSelected()) {
                response.put("MessageID", (String) this.jComboBox4.getSelectedItem());
            }
            if (this.jCheckBox5.isSelected()) {
                response.put(this.jTextField1.getText(), this.jTextField2.getText());
            }
            if (this.jCheckBox6.isSelected()) {
                response.put(this.jTextField3.getText(), this.jTextField4.getText());
            }
            if (this.jCheckBox7.isSelected()) {
                response.put(this.jTextField5.getText(), this.jTextField6.getText());
            }
            if (this.jCheckBox8.isSelected()) {
                response.put(this.jTextField7.getText(), this.jTextField8.getText());
            }

            StringBuilder sbuf = new StringBuilder();
            ObjectMapper mapper = new ObjectMapper();
            //sbuf.append("<json>");
            sbuf.append(mapper.writeValueAsString(response));
            //sbuf.append("</json>");
            this.jTextArea1.setText(sbuf.toString());

            if (this.main != null) {
                this.main.setSendText(sbuf.toString());
            }

        } catch (JsonProcessingException ex) {
            Logger.getLogger(JsonMakerAI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

